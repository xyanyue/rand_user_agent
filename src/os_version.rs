use std::fmt::Display;

use rand::{distributions::Standard, prelude::Distribution, Rng};
// "3.1", "3.5", "3.51", "4.0", "5.0", 基本很少
const WIN_NT: [&str; 8] = ["5.1", "5.2", "6.0", "6.1", "6.2", "6.3", "6.4", "10.0"];
const WIN_BIT: [&str; 3] = ["Win64; x64", "WOW64", ""];
const MACINTOSH_VER: [&str; 19] = [
    "13_3_1", "12_6_5", "11_7_6", "10_15_7", "10_14_6", "10_13_6", "10_12_6", "10_11_6", "10_10_5",
    "10_9_5", "10_8_5", "10_7_5", "10_6_8", "10_5_8", "10_4_11", "10_3_9", "10_2_8", "10_1_5",
    "10_0_4",
];
const LINUX_VER: [&str; 3] = ["CrOS", "Ubuntu", "Linux"];
const ANDROID_VER: [&str; 4] = ["9", "10", "11", "12"];

pub enum OS {
    Windows,
    Macintosh,
    Linux,
    Android,
    Ios,
}

impl OS {
    pub fn random<R: Rng + ?Sized>(rng: &mut R) -> Self {
        match rng.gen_range(0..5) {
            0 => OS::Windows,
            1 => OS::Macintosh,
            2 => OS::Linux,
            3 => OS::Android,
            4 => OS::Ios,
            _ => OS::Android,
        }
    }

    pub fn mobile<R: Rng + ?Sized>(rng: &mut R) -> Self {
        match rng.gen_range(0..2) {
            0 => OS::Android,
            1 => OS::Ios,
            _ => OS::Android,
        }
    }
    pub fn pc<R: Rng + ?Sized>(rng: &mut R) -> Self {
        match rng.gen_range(0..3) {
            0 => OS::Windows,
            1 => OS::Macintosh,
            2 => OS::Linux,
            _ => OS::Windows,
        }
    }
}

impl Distribution<OS> for Standard {
    fn sample<R: Rng + ?Sized>(&self, rng: &mut R) -> OS {
        OS::random(rng)
    }
}

impl Display for OS {
    fn fmt(&self, f: &mut std::fmt::Formatter<'_>) -> std::fmt::Result {
        let mut rng = rand::thread_rng();
        match self {
            OS::Windows => write!(
                f,
                "(Windows NT {};{})",
                WIN_NT[rng.gen_range(0..WIN_NT.len())],
                WIN_BIT[rng.gen_range(0..WIN_BIT.len())],
            ),
            OS::Macintosh => write!(
                f,
                "(Macintosh; Intel Mac OS X {})",
                MACINTOSH_VER[rng.gen_range(0..MACINTOSH_VER.len())],
            ),
            OS::Linux => write!(
                f,
                "(X11; {} x86_64)",
                LINUX_VER[rng.gen_range(0..LINUX_VER.len())],
            ),
            OS::Android => write!(
                f,
                "(Linux; Android {}; {})",
                ANDROID_VER[rng.gen_range(0..ANDROID_VER.len())],
                ANDROID_OS[rng.gen_range(0..ANDROID_OS.len())],
            ),
            OS::Ios => write!(f, "(iPhone; CPU iPhone OS 16_3_1 like Mac OS X)"),
        }
    }
}
